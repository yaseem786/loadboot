-- cvb_delivery_engine_core.sql
-- UNIFIED marketing/transactional delivery engine — the ONE shared architecture for ALL channels.
-- Deliberately a single delivery ledger (message_deliveries) for email + sms + future channels, rather than
-- separate incompatible per-channel tables. Consent lives in comm_preferences; suppressions is the global
-- opt-out/bounce list; provider_events is the idempotent provider-webhook sink.
--
-- Security: all public cc_* functions are SECURITY DEFINER and gated on app_private.can_manage_comms()
-- (content.manage OR settings.manage). Default PUBLIC execute is revoked; only authenticated may call.
-- The internal app_private.* helpers are never granted to anon/authenticated (schema is deny-by-default).
--
-- Applied to: staging (snslhvmkjusozgjelghi) and production (rwscphuhpjoudvljvmdk)
--   as consolidated migration `cvb_cvc_delivery_engine`.

-- ---------------------------------------------------------------------------
-- 1. Unified delivery ledger (one row per recipient per channel per campaign/source)
-- ---------------------------------------------------------------------------
create table if not exists app_private.message_deliveries (
  id               uuid primary key default gen_random_uuid(),
  org_id           uuid,
  source           text not null default 'campaign',   -- campaign | transactional | test
  campaign_id      uuid,
  template_key     text,
  template_version integer,
  channel          text not null,                       -- email | sms
  provider         text,                                -- resend | twilio | ...
  recipient_user   uuid,
  recipient_email  text,
  recipient_phone  text,
  correlation_id   uuid not null default gen_random_uuid(),
  idempotency_key  text not null,
  status           text not null default 'queued',      -- scheduled|queued|claimed|sent|delivered|opened|clicked|bounced|complained|unsubscribed|failed|dead_letter
  attempts         integer not null default 0,
  scheduled_at     timestamptz,
  claimed_at       timestamptz,
  sent_at          timestamptz,
  delivered_at     timestamptz,
  failure_reason   text,
  related_carrier  uuid,
  related_partner  uuid,
  related_lead     uuid,
  related_load     uuid,
  related_trip     uuid,
  meta             jsonb not null default '{}'::jsonb,
  created_at       timestamptz not null default now(),
  updated_at       timestamptz not null default now()
);
create unique index if not exists md_idem_uq    on app_private.message_deliveries (idempotency_key);
create index        if not exists md_queue_idx  on app_private.message_deliveries (status, scheduled_at);
create index        if not exists md_campaign_idx on app_private.message_deliveries (campaign_id);

-- ---------------------------------------------------------------------------
-- 2. Global suppression list (hard opt-outs, bounces, complaints) — enforced at enqueue time
-- ---------------------------------------------------------------------------
create table if not exists app_private.suppressions (
  id         uuid primary key default gen_random_uuid(),
  channel    text not null,                             -- email | sms
  address    text not null,                             -- stored lower-cased
  reason     text,                                      -- bounced | complained | manual | unsubscribed
  created_at timestamptz not null default now()
);
create unique index if not exists supp_uq on app_private.suppressions (channel, lower(address));

-- ---------------------------------------------------------------------------
-- 3. Provider webhook events (idempotent sink; dedupe_key de-duplicates re-delivered webhooks)
-- ---------------------------------------------------------------------------
create table if not exists app_private.provider_events (
  id                bigint generated by default as identity primary key,
  delivery_id       uuid,
  provider          text,
  raw_type          text,
  normalized_status text,
  dedupe_key        text,
  payload           jsonb,
  occurred_at       timestamptz not null default now()
);
create unique index if not exists pe_dedupe_uq on app_private.provider_events (dedupe_key) where dedupe_key is not null;

-- ---------------------------------------------------------------------------
-- 4. Authorization helper — who may manage comms/campaigns
-- ---------------------------------------------------------------------------
create or replace function app_private.can_manage_comms()
returns boolean language sql stable security definer set search_path to 'app_private, public'
as $$
  select public.has_global_permission('content.manage') or public.has_global_permission('settings.manage');
$$;

-- ---------------------------------------------------------------------------
-- 5. Audience → email resolver (single source of recipient truth for every audience type).
--    opted_in encodes consent: newsletter submitters are explicit opt-in (true); generic form
--    submitters and drivers are NOT marketing-opted-in by default (false); carriers honour their
--    comm_preferences (marketing_email and not unsubscribed_all).
-- ---------------------------------------------------------------------------
create or replace function app_private.resolve_audience_emails(p_type text)
returns table(recipient_user uuid, email text, opted_in boolean)
language plpgsql stable security definer set search_path to 'app_private, public'
as $$
begin
  if p_type='newsletter' then
    return query select null::uuid, lower(fs.email), true from app_private.form_submissions fs
      where fs.form_key='newsletter' and coalesce(fs.spam_score,0)<80 and fs.email ~ '^[^@]+@[^@]+\.[^@]+$' group by lower(fs.email);
  elsif p_type='form_submitters' then
    return query select null::uuid, lower(fs.email), false from app_private.form_submissions fs
      where coalesce(fs.spam_score,0)<80 and fs.email ~ '^[^@]+@[^@]+\.[^@]+$' group by lower(fs.email);
  elsif p_type in ('all_carriers','active_carriers','carrier_owners','pending_carriers') then
    return query select p.id, lower(p.email), (coalesce(cp.marketing_email,true) and not coalesce(cp.unsubscribed_all,false))
      from public.profiles p left join app_private.comm_preferences cp on cp.user_id=p.id
      where p.role='carrier' and p.email is not null and p.email ~ '^[^@]+@[^@]+\.[^@]+$'
        and (p_type<>'active_carriers' or p.status='active');
  elsif p_type='drivers' then
    return query select null::uuid, lower(d.email), false from app_private.fleet_drivers d
      where d.email is not null and d.email ~ '^[^@]+@[^@]+\.[^@]+$' group by lower(d.email);
  else return; end if;
end; $$;

-- can_manage_comms/resolve_audience_emails live in the deny-by-default app_private schema; no grants needed.
