-- cvy_trip_tracking.sql
-- Increments 50 + 51 — Tracking/location foundation + Trip Control Tower + broker permitted visibility.
-- Consent-first: a carrier must explicitly select a tracking method (recorded with a timestamp) before GPS
-- locations are accepted. Every location row is labeled with its SOURCE (no invented GPS, manual clearly
-- marked). Stale tracking is surfaced honestly; nothing auto-cancels a contractual load because GPS dropped.
--
-- Applied to: staging (snslhvmkjusozgjelghi) and production (rwscphuhpjoudvljvmdk).

create table if not exists app_private.trip_locations (
  id         bigint generated by default as identity primary key,
  trip_id    uuid not null,
  lat        double precision,
  lng        double precision,
  accuracy_m integer,
  source     text not null,   -- pocket_gps | manual_checkin | carrier_update | dispatcher_update | eld | telematics
  note       text,
  created_by uuid,
  created_at timestamptz not null default now()
);
-- a legacy trip_locations table pre-exists on both DBs (id uuid, label text); extend it additively
alter table app_private.trip_locations add column if not exists accuracy_m integer;
alter table app_private.trip_locations add column if not exists note text;
alter table app_private.trip_locations add column if not exists created_by uuid;
alter table app_private.trip_locations alter column lat drop not null;   -- note-only manual check-ins are allowed
alter table app_private.trip_locations alter column lng drop not null;
create index if not exists trip_locations_trip_idx on app_private.trip_locations(trip_id, created_at desc);
alter table app_private.trip_locations drop constraint if exists trip_locations_source_check;
-- 'carrier' kept for pre-existing legacy rows (additive; no data rewritten)
alter table app_private.trip_locations add constraint trip_locations_source_check
  check (source = any (array['pocket_gps','manual_checkin','carrier_update','dispatcher_update','eld','telematics','carrier']));

alter table app_private.trips add column if not exists tracking_method text;
alter table app_private.trips add column if not exists tracking_consent_at timestamptz;

-- Carrier selects a tracking method (explicit consent). Marks the booking-checklist item received.
create or replace function public.cc_trip_set_tracking(p_trip uuid, p_method text)
returns jsonb language plpgsql security definer set search_path to 'app_private, public'
as $$
declare v_org uuid; t record;
begin
  v_org := app_private.my_carrier_org();
  if v_org is null then raise exception 'not a carrier account' using errcode='42501'; end if;
  select * into t from app_private.trips where id=p_trip and carrier_id=v_org for update;
  if t.id is null then raise exception 'trip not found' using errcode='42501'; end if;
  if p_method <> all (array['pocket_gps','eld','telematics','manual_checkin']) then raise exception 'invalid tracking method' using errcode='22023'; end if;
  update app_private.trips set tracking_method=p_method, tracking_consent_at=now(),
    location_consent=(p_method<>'manual_checkin'), updated_at=now() where id=p_trip;
  update app_private.load_document_checklist set status='received', updated_at=now()
    where subject_type='load' and subject_id=t.load_id and doc_key='tracking_method';
  perform app_private.emit_event('trip.tracking_selected','trip',p_trip::text, jsonb_build_object('trip',p_trip,'method',p_method));
  perform app_private.log_audit('trip.tracking','trip',p_trip::text,null,'tracking method selected: '||p_method,
    jsonb_build_object('consent', p_method<>'manual_checkin'));
  return jsonb_build_object('ok',true,'method',p_method,'consent_recorded',true);
end; $$;
revoke execute on function public.cc_trip_set_tracking(uuid, text) from anon, public;
grant  execute on function public.cc_trip_set_tracking(uuid, text) to authenticated;

-- Carrier/driver check-in. GPS coordinates require prior consent (a tracking method other than manual).
-- A manual check-in (note, optional coords typed by a person) is always allowed and clearly labeled.
create or replace function public.cc_trip_checkin(p_trip uuid, p_lat double precision default null, p_lng double precision default null,
  p_note text default null, p_source text default 'manual_checkin')
returns jsonb language plpgsql security definer set search_path to 'app_private, public'
as $$
declare v_org uuid; t record;
begin
  v_org := app_private.my_carrier_org();
  if v_org is null then raise exception 'not a carrier account' using errcode='42501'; end if;
  select * into t from app_private.trips where id=p_trip and carrier_id=v_org for update;
  if t.id is null then raise exception 'trip not found' using errcode='42501'; end if;
  if p_source <> all (array['pocket_gps','manual_checkin','carrier_update']) then raise exception 'invalid source' using errcode='22023'; end if;
  if p_source='pocket_gps' and not coalesce(t.location_consent,false) then
    raise exception 'GPS tracking requires consent — select a tracking method first' using errcode='42501'; end if;
  insert into app_private.trip_locations(trip_id,lat,lng,source,note,created_by)
    values (p_trip,p_lat,p_lng,p_source,p_note,auth.uid());
  if p_lat is not null and p_lng is not null then
    update app_private.trips set last_lat=p_lat, last_lng=p_lng, last_loc_at=now(), updated_at=now() where id=p_trip;
  end if;
  perform app_private.emit_event('trip.location_updated','trip',p_trip::text,
    jsonb_build_object('trip',p_trip,'source',p_source,'has_coords', p_lat is not null));
  return jsonb_build_object('ok',true,'source',p_source);
end; $$;
revoke execute on function public.cc_trip_checkin(uuid, double precision, double precision, text, text) from anon, public;
grant  execute on function public.cc_trip_checkin(uuid, double precision, double precision, text, text) to authenticated;

-- Trip Control Tower (staff): active trips with freshness, consent, checklist gaps and the next required action.
create or replace function public.cc_control_tower(p_limit integer default 100)
returns table(trip_id uuid, load_id uuid, carrier text, origin text, destination text, status text,
  scheduled_pickup timestamptz, scheduled_delivery timestamptz,
  tracking_method text, location_consent boolean, last_lat double precision, last_lng double precision,
  last_loc_at timestamptz, loc_fresh_minutes integer, loc_source text,
  checklist_missing integer, open_exceptions integer, next_action text)
language plpgsql stable security definer set search_path to 'app_private, public'
as $$
begin
  if not public.has_global_permission('dispatch.view') then raise exception 'not authorized' using errcode='42501'; end if;
  return query
  select t.id, t.load_id, org.name, l.origin, l.destination, t.status,
    t.scheduled_pickup, t.scheduled_delivery,
    t.tracking_method, coalesce(t.location_consent,false), t.last_lat, t.last_lng, t.last_loc_at,
    (case when t.last_loc_at is not null then (extract(epoch from now()-t.last_loc_at)/60)::int end),
    (select tl.source from app_private.trip_locations tl where tl.trip_id=t.id order by tl.created_at desc limit 1),
    (select count(*) from app_private.load_document_checklist c where c.subject_type='load' and c.subject_id=t.load_id and c.status in ('required','rejected','expired'))::int,
    (select count(*) from app_private.trip_exceptions e where e.trip_id=t.id and coalesce(e.status,'open')='open')::int,
    (case
      when t.tracking_method is null then 'select tracking method'
      when exists (select 1 from app_private.load_document_checklist c where c.subject_type='load' and c.subject_id=t.load_id and c.status in ('required','rejected','expired')) then 'complete booking checklist'
      when coalesce(t.location_consent,false) and t.status in ('dispatched','in_transit')
           and (t.last_loc_at is null or t.last_loc_at < now() - interval '120 minutes') then 'stale tracking — request check-in'
      when t.status='planned' then 'dispatch trip'
      when t.status='dispatched' then 'confirm pickup'
      when t.status='in_transit' then 'monitor to delivery'
      else 'review' end)
  from app_private.trips t
  join public.organizations org on org.id=t.carrier_id
  left join public.loads l on l.id=t.load_id
  where t.status in ('planned','dispatched','in_transit')
  order by t.scheduled_pickup nulls last, t.created_at desc
  limit least(greatest(coalesce(p_limit,100),1),300);
end; $$;
revoke execute on function public.cc_control_tower(integer) from anon, public;
grant  execute on function public.cc_control_tower(integer) to authenticated;

-- Broker permitted visibility: status + freshness + own checklist. NO coordinates, NO driver personal data,
-- NO carrier financials or internals.
create or replace function public.cc_partner_load_status(p_partner_load uuid)
returns jsonb language plpgsql stable security definer set search_path to 'app_private, public'
as $$
declare v_org uuid; pl record; l record; t record; v_docs jsonb;
begin
  v_org := app_private.my_partner_org('broker');
  if v_org is null then raise exception 'not a broker account' using errcode='42501'; end if;
  select * into pl from app_private.partner_loads where id=p_partner_load and broker_org=v_org;
  if pl.id is null then raise exception 'load not found' using errcode='42501'; end if;
  if pl.posted_load_id is not null then
    select * into l from public.loads where id=pl.posted_load_id;
    select * into t from app_private.trips where load_id=pl.posted_load_id order by created_at desc limit 1;
  end if;
  select coalesce(jsonb_agg(jsonb_build_object('label',c.label,'status',c.status) order by c.label),'[]'::jsonb) into v_docs
    from app_private.load_document_checklist c
    where ((c.subject_type='partner_load' and c.subject_id=pl.id) or (c.subject_type='load' and c.subject_id=pl.posted_load_id))
      and c.required_from='broker';
  return jsonb_build_object(
    'submission_status', pl.status,
    'load_status', (case when l.id is not null then l.status end),
    'trip_status', (case when t.id is not null then t.status end),
    'carrier_assigned', (t.id is not null),
    'scheduled_pickup', t.scheduled_pickup, 'scheduled_delivery', t.scheduled_delivery,
    'tracking_state', (case
        when t.id is null then null
        when not coalesce(t.location_consent,false) and t.tracking_method is null then 'not configured'
        when t.last_loc_at is null then 'temporarily unavailable'
        when t.last_loc_at < now() - interval '120 minutes' then 'temporarily unavailable'
        else 'active' end),
    'tracking_fresh_minutes', (case when t.last_loc_at is not null then (extract(epoch from now()-t.last_loc_at)/60)::int end),
    'your_required_documents', v_docs);
end; $$;
revoke execute on function public.cc_partner_load_status(uuid) from anon, public;
grant  execute on function public.cc_partner_load_status(uuid) to authenticated;
